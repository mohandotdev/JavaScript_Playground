//Augumented Math Operations
//Compound Assignment with Augumented Addition
var num1 = 100;
num1 += 250;  //---> (num1 = num1 + 250)
console.log(num1);

//Compound Assignment with Augumented Subtraction
var num2 = 173;
num2 -= 23;  //---> (num2 = num2 - 23)
console.log(num2);

//Compound Assignment with Augumented Multiplication
var num3 = 125;
num3 *= 7;  //---> (num3 = num3 * 7)
console.log(num3);

//Compound Assignment with Augumented Multiplication
var num4 = 1678;
num4 /= 2;
console.log(num4);