/*****
Escape Sequence

\' sinle quotes
\" double quotes
\\ backslash
\n newline
\r carriage return
\t tab
\b backspace
\f form feed

*****/
var str1 = "\'Billy Butcher\'"; // --> \'single quotes
console.log(str1);

var str2 = "\"Homelander\""; // --> \" double quotes
console.log(str2);

console.log("\\"); // --> To print the backslash

console.log("The Boys\nBilly Butcher <--> Homelander"); // --> \n new line

console.log("Hello Javascript\rI am noob"); // \r --> carriage return

console.log("Hello\tWorld:)"); // \t --> tab space

console.log("Hello \bjavascript"); // \b --> backspace

console.log("One Two Three\f Four\f King"); // \f --> form feed