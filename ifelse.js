//if-else statement
function trueOrfalse(isItTrue)
{
    if(isItTrue)
    {
        return "Yes, it was true.";
    }
    return "No, it was false.";
}
console.log(trueOrfalse(true));

//Equality operator
function testEqual(num)
{
    if(num == 100)
    {
        return "Equal";
    }
    return "Not Equal";
}
console.log(testEqual(99));
